# type() 函数：获取指定字面量或变量的类型
print(type(1))
print(type(3.1415926))
print(type(True))
print(type(False))
print(type("hello"))
print(type(None))
print(type([]))
print(type({}))
print(type(()))

num = 20
print(type(num))

# isinstance() 函数：判断数据象是否是指定的类型
print(isinstance(num, int))
print(isinstance(num, float))
print(isinstance(num, str))
